<P>The league is now into its eighteenth season and is still going strong despite the advent of dozens of new sims.  GPL still attracts a loyal following and it now provides the opportunity to race in cars from the 1965, 1966 and 1969 seasons as well as in the original 1967 cars.  This season UKGPL will run two championships for 1965 cars, one for 1966 cars and one for the 1967 cars.  We will also be running a joint championship for the 1969 cars with our friends at <A HREF="http://www.gbsimracing.co.uk/">GBGPL</A>.  Alongside this we will also run a variety of other races against other leagues. Our aim is simply to have some fun racing those 1960s brutes. Originally league membership was restricted to United Kingdom based drivers because of the low latencies, hence enhanced game play, achieved by having all the drivers based in the UK.  With the advent of broad band, latencies are not the serious problem they once were. Consequently membership restrictions have been relaxed but all races are run at times most suited to UK based drivers.</P>

