<H1>Season 24 is under way. Registration is now OPEN!<br>
Don't miss out!</H1>

<P align="justify">
The league is now into its fourteenth year and is still going strong despite the advent of dozens of new sims.
None can hold a candle to the accuracy of Grand Prix Legends though, which still attracts a loyal following and it
now provides the opportunity to race in cars from the 1965, 1966 and 1969 seasons as well as in the original 1967 cars. The F2 cars and the Can-Ams prove that the community still has some life in it yet!</p>

<P align="justify">
This coming season UKGPL will run two championships for 1965 cars, one for 1966 cars, two for the 1967 cars (F1 and F2), a 69 series and something for fans of sports cars as well.
Our aim is simply to have some fun racing those 1960s brutes.</p>

<P align="justify">
We are now an international league having drivers from many countries but races are run at times most suited to UK based drivers.
There is something to suit everyone, both newcomers to online racing and those more experienced, so join our forum introduce yourself and enjoy the racing.</P>

