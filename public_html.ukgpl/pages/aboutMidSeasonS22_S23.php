<H1>Season 22 is concluded - Season 23 is coming!</H1>

<P align="justify">
The league is now into its thirteenth year and is still going strong despite the advent of dozens of new sims.
None can hold a candle to the accuracy of Grand Prix Legends though, which still attracts a loyal following and it
now provides the opportunity to race in cars from the 1965, 1966 and 1969 seasons as well as in the original 1967 cars. The F2 cars, the 69-Extra carset and the forthcoming Can-Am 71 Mod prove that the community still has plenty of life in it yet!</p>

<P align="justify">
This season UKGPL will run three championships for 1965 cars, one for 1966 cars, two for the 1967 cars and two for the 
1967 GT Mod, one being for Porsche only.  We will also be running championship for the 1967 F2 cars.
Our aim is simply to have some fun racing those 1960s brutes.</p>

<P align="justify">
We are now an international league having drivers from many countries but races are run at times most suited to UK based drivers.
There is something to suit everyone, both newcomers to online racing and those more experienced, so join our forum introduce yourself and enjoy the racing.</P>

<P align="justify">
Registration for the Season 23 races isn't open yet but we will be announcing that very soon. Please visit the <A HREF="http://www.simracing.org.uk/smf/index.php?board=48.0" target="_blank">announcements</A> forum.
Here you will be able to register for the forthcoming season.</p>

