<H1 ALIGN="CENTER">A Guide for the use of TeamSpeak with GPL</H1>

<H3>Install the TeamSpeak Client</H3> 

<P>TeamSpeak is a proprietary voice-over-Internet Protocol (VoIP) application. The TeamSpeak client can be downloaded from the TeamSpeak  <A HREF="https://www.teamspeak.com/">WebSite</A>. Follow the instructions on the website to install the client on your computer.</P>

<H3>Run TeamSpeak Client</H3>

<p>1. Run TeamSpeak and the following window should appear.</p>
<blockquote>
  <blockquote>
    <blockquote>
      <p><img border="0" src="/images/teamspeak/teamspeakMainWindow.jpg" width="650" height="567"></p>
      <p></p>
    </blockquote>
  </blockquote>
</blockquote>

<p>2. Select &quot;Connect&quot; from the &quot;Connections&quot; drop down list:</p>
<blockquote>
  <blockquote>
    <blockquote>
    <p><img border="0" src="/images/teamspeak/teamspeakConnectDropdown.jpg" width="650" height="392"></p>
    </blockquote>
  </blockquote>
</blockquote>

<p>3. Enter the TeamSpeak Server IP address (for UKGPL__4 this will be the same address as the race server), enter a Nickname (use your UKGPL forum name so other drivers know who you are) and the password.  The password is case-sensitive and for UKGPL__4 it will normally be set to be the same as the race password.</p>
<blockquote>
  <blockquote>
    <blockquote>
    <p><img border="0" src="/images/teamspeak/teamspeakConnectPopup.jpg" width="650" height="468"></p>
    </blockquote>
  </blockquote>
</blockquote>

<p>4. Once connected, the available channels will appear in the Client window.  On UKGPL__4 the default channel is "Open Chat". There is a chat channel for each of the teams. These are not password protected so it is possible to eavesdrop on other teams as it is in real F1.</p>
<blockquote>
  <blockquote>
    <blockquote>
    <p><img border="0" src="/images/teamspeak/teamspeakMainWindowConnected.jpg" width="650" height="504"></p>
    </blockquote>
  </blockquote>
</blockquote>

<p>5. There are a multitude of options in TeamSpeak, too many to discuss here, particularly since most are dependent the specific installation. However it is worth explaining how to map the Hotkey option for a couple of useful features, &quot;Toggle Speaker Mute&quot; and &quot;Push-to-Talk, Default&quot;. In the &quot;Tools&quot; drop down list, select &quot;Options&quot;:</p>
<blockquote>
  <blockquote>
    <blockquote>
    <p><img border="0" src="/images/teamspeak/teamspeakOptionsDropdown.jpg" width="650" height="479"></p>
    </blockquote>
  </blockquote>
</blockquote>

<p>6. Click on the HotKeys option in the lefthand pane and a popup should appear. Click on the &quot;Add&quot; button to assign a Hotkey.</p>
<blockquote>
  <blockquote>
    <blockquote>
    <p><img border="0" src="/images/teamspeak/teamspeakOptionsHotKeysAdd.jpg" width="650" height="455"></p>
    </blockquote>
  </blockquote>
</blockquote>

<p>7. The &quot;Hotkey Setup Dialogue&quot; popup window should appear. Scroll down and expand the &quot;Speaker&quot; option. Under this option there will be an option &quot;Toggle Speaker Mute&quot;.</p>
<blockquote>
  <blockquote>
    <blockquote>
    <p><img border="0" src="/images/teamspeak/teamspeakOptionsDialogue1.jpg" width="400" height="381"></p>
    </blockquote>
  </blockquote>
</blockquote>

<p>8. Click on the &quot;Toggle Speaker Mute&quot; option. Then by clicking on the assign button, this option can be mapped to a key of your choice. Ideally a button on your steering wheel.</p>
<blockquote>
  <blockquote>
    <blockquote>
    <p><img border="0" src="/images/teamspeak/teamspeakOptionsDialogue2.jpg" width="500" height="304"></p>
    </blockquote>
  </blockquote>
</blockquote>

<p>9.  In this case Button 5 on the steering wheel.</p>
<blockquote>
  <blockquote>
    <blockquote>
    <p><img border="0" src="/images/teamspeak/teamspeakOptionsDialogue3.jpg" width="400" height="385"></p>
    </blockquote>
  </blockquote>
</blockquote>

<p>10. Follow the same process to assign the &quot;Push-to-Talk, Default&quot; option to another button on your steering wheel. Then click on the &quot;OK&quot; button commit the changes.</p>
<blockquote>
  <blockquote>
    <blockquote>
    <p><img border="0" src="/images/teamspeak/teamspeakOptionsDialogue4.jpg" width="300" height="289"></p>
    </blockquote>
  </blockquote>
</blockquote>

<p>11. Once these buttons are set up it will be possible to easily turn TeamSpeak on and off during the race if the chat gets too irritating or distracting. The &quot;Press-to-Talk&quot; facility will ensure background noises will not distract the other drivers. During the race you will obviously need to press the button to enable your microphone in order for you to talk to the other drivers.</p>
