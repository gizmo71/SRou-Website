<H1 ALIGN="CENTER">A Guide for New Members</H1>

<H3>Welcome to UKGPL</H3> 

<P>The first thing you will need to do is <A HREF="//www.simracing.org.uk/smf/index.php?action=help;page=registering">Register</A> on our forums.  Once you have registered, go to our <A HREF="https://www.simracing.org.uk/smf/index.php?topic=6787.msg118356#msg118356">New Drivers</A> thread in the Announcements section which contains a guide to getting started and is the place to get help and advice when starting with UKGPL.</P>

<H3>IRC/WinVROC/iGOR/TeamSpeak</H3>

<P>You will also need either an IRC client, or a copy of <A HREF="http://www.vroc.net/"> WinVROC</A> or <A HREF="http://gem.autosimsport.net/">iGOR</A>.  We very rarely use VROC now, most of our races are hosted via iGOR. Before the races we normally meet in the UKGPL chatroom (the VROC and iGOR chat rooms are actually IRC channels on <A HREF="http://www.shadowworld.net/"> ShadowWorld</A>).</P>

<P>If you only use IRC, you can still join the race server without WinVROC/iGOR by typing the IP address directly into GPL via the Multiplayer menu.</P>

<P>TeamSpeak is a proprietary voice-over-Internet Protocol (VoIP) application that can be used to hold conversations between drivers whilst racing GPL. When used, a Teamspeak server will be provided by the race organiser but to use the system each driver will need to download and install the <A HREF="https://www.teamspeak.com">Teamspeak Client</A>. Guidelines on how to use TeamSpeak with GPL are <A HREF='/pages/teamSpeak.php'>here</A>.</P>

<H3>Tuned hardware</H3>

<P>At UKGPL we run all our races at 60 frames per second (fps). A patch is needed to enable GPL to run at 60fps, details on how to install the patch are available in the <A HREF="https://www.simracing.org.uk/smf/index.php?topic=6787.msg118356#msg118356">New Drivers</A> thread. Make sure you can get a steady 60 fps when running GPL online (or as close as your hardware allows). If you get less, it will compromise the quality of your connection and will spoil the racing for both you and other competitors.</P>

<H3>Tuned connection</H3>

<P>Finally, you need a <B>good quality connection</B>. The central driving force behind the creation of UKGPL was the idea that by restricting the series to UK-based drivers and using a UK-based server, we could keep connection quality up and latency down to enjoy high quality racing, greatly reducing the warping and clock smashes which characterize GPL internet play.   However with the faster connection speeds afforded by modern broadband facilites we can now welcome drivers worldwide but the race times will be best suited to UK based drivers.</P>

<H3>Trouble Shooting</H3>

<P>If after following all the guidelines in the <A HREF="https://www.simracing.org.uk/smf/index.php?topic=6787.msg118356#msg118356">New Drivers</A> thread you are experiencing problems, there is help available from the other UKGPL Members via the <A HREF="https://www.simracing.org.uk/smf/index.php#6">forums</A>. Typical areas where drivers experience problems and specific help is available are:
<UL>
  <LI> Windows 10 and updates.
  <LI> Linux.
  <LI> <A HREF="https://www.simracing.org.uk/smf/index.php?topic=12808.0">Framerate using the 60fps patch</A>.
  <LI> <A HREF="https://www.simracing.org.uk/smf/index.php?topic=24321.0">Bad framerate on-line</A>.
  <LI> Crash to Desktop (CTD).
  <LI> Screen Freeze.
  <LI> Steering Wheels and USB connections.  
  <LI> <A HREF="https://www.simracing.org.uk/smf/index.php?topic=23771.0">iGOR</A>.  
</UL>
</P>

<P>
There are 3 ways on-line races end prematurely. It is important to make a note of exactly what happened, because the remedy in each case will be different:
<UL>
   <LI>Crash to Desktop (CTD) - GPL stops mid race, shuts down without prompting and the user is returned to the Operating System (desktop) not iGOR.
   <LI>Screen Freeze - GPL stops mid race, the graphics appear frozen and the sound turns into a buzz that increases in volume and pitch. The only way to get out of this situation is to use Task Manager to kill GPL or to do a hardware reset (i.e power off the PC).
   <LI>Disco - The client is disconnected from the server. GPL stops mid race and returns to the GPL Multiplayer menu. The user can then close GPL using the normal red button and the user is returned to iGOR.
</UL>
</P>

