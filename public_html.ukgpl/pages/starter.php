<H1 ALIGN="CENTER">A Guide for New Members</H1>

<H2>What You Need</H2>

<H3>GPL itself</H3>

<P>First and foremost, you need Grand Prix Legends by Papyrus/Sierra. This is now available from many outlets for under £5.</P>

<P>You will also need various patches; please see the <A HREF="rules/technical"> Technical Regulations</A> for details.</P>

<H3>IRC/WinVROC</H3>

<P>You will also need either an IRC client, or a copy of <A
HREF="http://www.vroc.net/"> WinVROC</A> (or both); before
races we normally meet in the UKGPL chatroom (the VROC chat rooms are
actually IRC channels on <A HREF="http://www.shadowworld.net/"> ShadowWorld</A>).
It's passworded - you should get the password when you join the mailing list.</P>

<P>If you use IRC, you can still join the race server without WinVROC by typing the IP address directly into GPL.</P>

<H3>Tuned hardware</H3>

<P>Make sure you can get a steady 36 frames per second when running GPL
online (or as close as your hardware allows). If you get less, it will
compromise the quality of your connection and will spoil the racing for both
you and other competitors. The VROC website has extensive information on
tuning your hardware for optimal performance.</P>

<H3>Tuned connection</H3>

<P>Finally, you need a <B>good quality connection</B>. The central driving
force behind the creation of UKGPL was the idea that by restricting the
series to UK-based drivers and using a UK-based server, we could keep
connection quality up and latency down to enjoy high quality racing, greatly
reducing the warping and clock smashes which characterize GPL internet
play.</P>
