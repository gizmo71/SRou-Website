<H1 ALIGN="CENTER">Structure</H1>

<H2>General</H2>

<UL>

<LI><P>UKGPL will be run as a series of championships.  Each championship may use the original 1967 carset or
any of the modified carsets.</P>

<LI><P>Where numbers of drivers allow, a championship may be split into two or more divisions.  Drivers will be allocated to a division based on their race ability as perceived by the moderator.</P>

<LI><P>Each championship (or division within a championship) will have it's own moderator who, ideally, will not be 
active in the division they moderate.</P>

<LI><P>If a driver is considered much too fast for their allocated
division they may, at the moderators discretion, be moved to another
division. (All points scored in a previous division will remain in
that division and a fresh score-sheet made for the new division.)
<B>NB.</B> This is not a right and will only be considered in
exceptional circumstances.</P>

</UL>

<H2>Handicapping and Chassis Levelling</H2>

<P>In order to promote closer racing or to discourage single chassis races, the moderators may employ a handicapping or chassis levelling scheme.  Where such a scheme is used the details are given on the championship page for the division.</P>

<H3>Handicapping</H3>
<UL>
<LI><P>Handicapping is used to encourage closer racing by limiting the faster drivers to the slower cars.</P>
<LI><P>Common handicapping schemes are:</P>
<UL>
<LI><P>Chassis Allocation - The moderator assesses the ability of a driver and allocates a single chassis to them for the season.</P>
<LI><P>Championship Position - The drivers at the top of the championship are limited to the slower chassis.  Numerous permutations of this scheme are possible .</P>
</UL>
</UL>

<H3>Chassis Levelling</H3>
<UL>
<LI><P>Chassis Levelling is used to encourage the use of the slower cars without a driver incurring a disadvantage over another driver using a faster chassis.</P>
<LI><P>Common Chassis Levelling schemes are:</P>
<UL>
<LI><P>Tokens - Each driver receives an allocation of tokens for the season. Each driver receives the same number of tokens and each chassis costs a different number of tokens.  The faster cars are more expensive.  Whilst a driver in a slower cheaper car will be at a disadvantage for a particular race, over the course of a season the advantages and disadvantages even out. So every driver has an equal chance of winning the championship.</P>

<LI><P><a href="pitstops">Pit Stops</a> - The drivers are free to pick any chassis but the faster chassis are subject to a timed pit stop.  The faster the car, the longer the pit stop. In theory all drivers, no matter what chassis they choose, will have an equal chance of winning the race.  Having a equal chance to win each race also means the drivers have an equal chance of winning the championship.</P>
</UL>
</UL>

<H2>Moderation</H2>

<UL>
<LI><P>All championship races will be moderated and action will be taken against drivers who contravene the rules.</P>

<LI><P>The severity of the offence will dictate the action taken against
the driver.</P>

<LI><P>Appeals against a decision made by the division moderator can be
made to the Chief Moderator who will appraise the incident and
give a final verdict in association with the other division
moderators. (If the moderator of the penalised driver's division is
also the Chief Moderator, the highest available division
moderator will be given the ultimate decision). A simple Personal Message (PM) 
on SimRacing.org.uk (SRou) to the Chief Moderator is sufficient to launch an appeal. The current Chief 
Moderator is listed on the <A HREF="http://www.ukgpl.com/index.php" target="_blank">UKGPL Homepage</A>.

<LI><P>Post-race penalties can include:
<UL>
<LI>Warning
<LI>Time/position penalty
<LI>Points loss
<LI>Suspended race ban
<LI>Race ban
<LI>Multiple race ban
<LI>Total ban from UKGPL
</UL></P>

<LI><P>Please note that the moderators will also, from time to time, be
reviewing the race replays themselves.</P>

<LI><P>Only reported incidents are guaranteed to be reviewed. It is generally up to the moderator to decide
how much time is devoted to reviewing the race in order to identify unreported incidents. The various options available to the moderator are described on the <a href="/index.php/moderating/moderatingarace">Moderating a Race</a> page.</P>

<BLOCKQUOTE>Drivers can only appeal against a penalty if they have
submitted an incident report.  So if you cause an incident or think it may
go against you it is just as important that you submit your side of
the story as it is for the victim.</BLOCKQUOTE>

<BLOCKQUOTE>Use the court system when submitting an incident report to your divisional
moderator.  There is always a link below the race results table for this purpose. If you think warp or the number of cars visible ahead/behind was a factor please make sure you attach a zipped clip of
the incident from your personal replay. If you forgot to save the
replay, the mod can't see the points in your favour so it is in your
own interests to save your own replay. If you disco'd from the race or
were otherwise unable to save a replay say so in your race report so
this can be taken into account.</BLOCKQUOTE>

<BLOCKQUOTE>Ideally all moderated race results should be published before the
next race for that division, in the event that a moderator finds
themselves unable to complete the report in time they should give
advance warning to the other mods so that someone else can complete
the report if possible. Any appeals must be submitted within 7 days of the
moderated report being published.</BLOCKQUOTE>

<BLOCKQUOTE>If you have any questions about what constitutes a 'fault'
incident your first enquiry should be to your divisional moderator but,
as a general rule, loss of control (related to your level of ability)
is not penalised, but poor decisions (optimistic passing attempts etc)
are.</BLOCKQUOTE>

</UL>