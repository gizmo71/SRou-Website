<H3>Ambitious Overtake</H3>

<p> The key to determining whether an incident is an ambitious
overtake or not is simply down to how far back the overtaking driver was when he
began the move and whether he achieved some overlap at turn-in.
Occasionally a driver may jump at an opportunistic moment to attempt a pass that
was not really on, perhaps when the leading car makes a small mistake but then
recovers almost immediately. Such an occasion could also be classed as an
ambitious overtake.</p>
<p>Some discretion may be afforded if there was a vast
difference in ability between the drivers and whether or not the overtaking
driver would have made the corner.</p>

<h4>Typical Scenarios:</h4>

<P>Impossibly Ambitious Overtake: Penalty 3 Places
<ol>
  <li> The overtaking driver had not achieved any degree of overlap at turn-in.
    The overtaking driver would NOT have made the corner. Penalty for
    overtaking driver.</li>
</ol>

<P>Very Ambitious Overtake: Penalty 2 Places
<ol>
  <li> The overtaking driver had not achieved any degree of overlap at turn-in.
    The overtaking driver WOULD have made the corner. Penalty for
    overtaking driver.</li>
</ol>

<P>Simple Ambitious Overtake: Penalty 1 Place
<ol>
  <li>The overtaking car had NOT achieved a 50% or greater overlap at the
  point of turn-in. There was no loss of control by either driver. Penalty for overtaking
    driver.</li>
  <li>The overtaking car HAD achieved a 100% or greater overlap at the
  point of turn-in. There was no loss of control by either driver. Penalty for overtaken
    driver.</li>
  <li>The overtaking driver had not achieved any degree of overlap at turn-in.
    The overtaken driver did NOT contest the place. The overtaking driver
    made a clean pass but subsequently crashed trying to make the corner.
    The overtaken driver then gets caught up in the carnage. Penalty for
    overtaking driver.</li>
</ol>

<P>Marginal Ambitious Overtake:  Warning
<ol>
  <li>In a corner, the overtaking car HAD achieved a 100% or greater overlap at the point
  of turn-in but the driver was not in full control but WOULD have made the
    corner.
  Warning for overtaking car.</li>
</ol>

<p>Contested Overtake: Racing Incident</p>
<ol>
  <li>The overtaking car had achieved a 50% or greater overlap but not 100%
  or greater at the point of turn-in and both drivers were in full
  control.</li>
  <li>The overtaking driver had not achieved any degree of overlap at turn-in.
    The overtaken driver DID contest the place by taking a defensive line.
    The overtaking driver made a clean pass but subsequently crashed trying to
    make the corner. The overtaken driver then gets caught up in the
    carnage.</li>
</ol>