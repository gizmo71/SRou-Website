<H2>Contact occurs</H2>

<p>Car A and Car B collide.</p> 

<H3>Overhead view:</H3>
<p>The collision was caused by Car A cutting back onto the racing line. Having run wide, Car A had presented an opportunity and Car B had every right to attempt a pass. 
	At the apex both cars were fighting for position and neither car had the right to the racing line.  Both cars should have remained on their own side of the track. 
	Car B did remain on the inside so no blame can be apportioned to Car B.  Car A moved over and consequently is liable to a penalty.</p> 

