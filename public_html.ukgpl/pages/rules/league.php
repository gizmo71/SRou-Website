<H1 ALIGN="CENTER">League Structure and Membership</H1>

<H2>General</H2>

<UL>

<LI CLASS="emAbove">This league will be run in a series of divisions based on 
	race ability as perceived by the moderator.</LI>

<LI CLASS="emAbove">Each division will have it's own moderator who will not be 
	active in the division they moderate.</LI>

<LI CLASS="emAbove">At the end of each season the the final standings will be used to
	decide who will be promoted/relegated to the next higher/lower division.</LI>

<LI CLASS="emAbove">New members joining mid season will have to wait for either 
	a slot to become available in a division or for enough new members to
	justify forming a new division.</LI>

<LI CLASS="emAbove">If a driver is considered much too fast for the entry level
	division they may, at the moderators discretion, be moved to another
	division. (All points scored in a previous division will remain in
	that division and a fresh score-sheet made for the new division.)
	<BR/><B>NB.</B> This is not a right and will only be considered in exceptional circumstances.</LI>

</UL>

<H2>Driving standards</H2>

<UL>

<LI CLASS="emAbove">Repeated poor driving standards will not be tolerated. Racing
accidents can and will happen, it is aggressive driving incidents
which can be avoided that will be penalised. The final decision is up
to the moderator of each division.</LI>

<LI CLASS="emAbove">The severity of the offence will dictate the action taken against
the driver.</LI>

<LI CLASS="emAbove">Appeals against a decision made by the division moderator can be
made to the chief moderator who will appraise the incident and
give a final verdict in association with the other division
moderators. (If the moderator of the penalised driver's division is
also the chief moderator, the highest available division
moderator will be given the ultimate decision).</LI>

<LI CLASS="emAbove">Post-race penalties can include:<UL>
	<LI>Warning
	<LI>Time/position penalty
	<!-- NOT CURRENTLY AVAILABLE LI>Points loss -->
	<LI>Exclusion from the results
	<LI>Suspended race ban
	<LI>Race ban
	<LI>Multiple race ban
	<LI>Total ban from UKGPL
</UL></LI>

<LI CLASS="emAbove">Please note that the moderators will also, from time to time, be reviewing the race replays themselves.</LI>

<LI CLASS="emAbove">Incidents should be reported using the <NOBR>&quot;Submit an incident report&quot;</NOBR> link found underneath the published results of the race.</LI>

<LI CLASS="emAbove">For all Divisions and the Master's Cup, only reported incidents are guaranteed to be reviewed.
It is generally up to the moderator to decide how much time is devoted to reviewing the race in order to identify unreported incidents.
The various options available to the moderator are described on the <a href="/index.php/moderating/moderatingarace">Moderating a Race</a> page.
However, for the Clubman's Cup the moderator would be expected to formally review the race rather than simply rely on reported incidents (the rationale here being
that newcomers to UKGPL usually start off in the Clubman's Cup and it is important that their driving standards are monitored to ensure they meet UKGPL standards).
In any case if a driver wants to ensure an incident is reviewed they should submit an incident report within 3 days of the race.
	<BLOCKQUOTE>Since you can only appeal against a penalty if you have submitted an incident report, if you cause an incident or think it may
		go against you it is just as important that you submit your side of the story as for the victims.</BLOCKQUOTE>
	<BLOCKQUOTE>When submitting an incident report, if you think warp or the number of cars visible
		ahead/behind was a factor please make sure you attach a zipped clip of the incident from your personal replay (this can be attached when submitting your report).
		If you forgot to save the replay the mod can't see the points in your favour so it is in your own interests to save your own replay.
		If you disco'd from the race or were otherwise unable to save a replay say so in your race report so this can be taken into account.</BLOCKQUOTE>
	<BLOCKQUOTE>Ideally all moderated race results should be published before the
		next race for that division; in the event that a moderator finds
		themselves unable to complete the report in time they should give
		advance warning to the other mods so that someone else can complete
		the report if possible. Any appeals must be submitted within 7 days of the
		moderated report being published.</BLOCKQUOTE>
	<BLOCKQUOTE>If you have any questions about what constitutes a 'fault'
		incident your first enquiry should be to your divisional moderator but,
		as a general rule, loss of control (related to your level of ability)
		is not penalised, but poor decisions (optimistic passing attempts etc)
		are.</BLOCKQUOTE>
</LI>

</UL>

<H2>New members</H2>

<UL>

<LI CLASS="emAbove">New members should send a PM to the club secretary (see staff list below) saying that they wish to join UKGPL and compete in the events.
They should include their <A HREF="http://gplrank.schuerkamp.de/" TARGET="_top"> GPLRank</A> if available by edited their
<A HREF="http://www.simracing.org.uk/smf/index.php?action=profile;sa=driver_info">Driver Details</A>.</LI>

<LI CLASS="emAbove">In order to maintain a high standard of racing, new members will
be expected to participate in at least two non-championship 'fun'
races before being permitted to race in the championship. This is so
that we can assess both fitness to race (basic car control and
attitude towards other cars on the track) and laptimes (to determine
which division would be best).</LI>

</UL>

