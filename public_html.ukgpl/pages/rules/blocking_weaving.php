<H3>Blocking/Weaving</H3>

<P>The guidelines are consistent with the long standing rule in "Racing Etiquette" which governs 
blocking and weaving.  This means moving off the racing line and then back on is not blocking 
providing it is NOT done in response to the movements of the following car. Also a following 
car would have to be in a position to overtake for any move by the lead car to be classed as 
blocking. The lead car cannot be blocking if the following car is a long way back (as a guide 
5 car lengths or more would be a long way back). "Committed to his line" means no opportunity 
to change lines before the braking zone.

<P>The key features to consider here are:<UL>
<LI> Did the blocker change line once, twice or many times?
<LI> Did the blocker move before the victim was committed to his change of line?
<LI> Taking a defensive line does not constitute blocking.
</UL>

<h4>Typical Scenarios:</h4>

<P>Appalling Blocking/Weaving: Penalty 3 Places
<ol>
  <li> The blocking driver continually (i.e. more than twice) weaved from side to
  side in response to the following cars movements thus taking every opportunity
  to block. Contact doesn't need to be made.</li>
</ol>

<P>Blatant Blocking/Weaving: Penalty 2 Places
<ol>
  <li> The blocking driver changed his line TWICE in RESPONSE to the following cars movements thus blocking on two separate occasions.
    Contact doesn't need to be made.</li>
  <li> The blocking driver changed his line in RESPONSE to the following car's movement and after the following car was committed to his line.
    Contact is made.</li>
</ol>

<P>Simple Blocking/Weaving: Penalty 1 Place
<ol>
  <li> The blocking driver changed his line ONCE in& RESPONSE to the following car's movement and
    AFTER the following car was committed to his line. No contact is made.</li>
</ol>

<P>Marginal Blocking/Weaving: Caution
<ol>
  <li> The blocking driver changed his line ONCE in RESPONSE to the following car's movement but well
    BEFORE the following car was committed to his line. No contact is made.</li>
</ol>