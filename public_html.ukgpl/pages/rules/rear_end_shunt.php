<H3>Rear End Shunt</H3>

<P>This is one of the most common incidents in GPL so there are plenty of previous cases which have set a precedent.

<P> The key features to consider here are:
<ul>
  <li>Was the shunter in control?</li>
  <li>Was the victim in control?</li>
  <li>Was the victim holding their normal line and speed?</li>
</ul>
<p>In all cases, the following car is responsible for maintaining a safe gap and the driver needs to be cognizant of the possibility of the lead car slowing for no apparent reason. Typical situations would be: passing the pit lane entry during the pit stop window and the early stages of lap one where incidents can occur at the head of the field out of the sight of the tail enders. </p>

<h4>Typical Scenarios:</h4>

<P>Blatant Rear End Shunt:  Penalty 2 Places
<ol>
  <li> The victim made no mistakes and was driving their normal line at normal speed.  The shunter made no allowance for the victim whatsoever. The shunter was in full 
control.</li>
</ol>

<P>Simple Rear End Shunt:  Penalty 1 Place
<ol>
  <li>The victim was in full control but braked early (not counting lap 1)
    compared with the average for the division. The shunter had not
    followed the victim at a safe distance for a lap or two. The shunter
    was in full control.</li>
  <li> The victim had partially lost control
before contact and consequently his line was unusual. The shunter was in
    full control.</li>
  <li> The victim made no mistakes and was driving their normal line at normal speed.
    The shunter had lost control on the approach to the braking zone and
  consequently braked later than intended.</li>
</ol>

<P>Marginal Rear End Shunt:  Warning
<ol>
  <li> The shunter had followed the victim for a lap or two before hand and the
victim had changed his braking point. Both the victim and the shunter were
    in full control.</li>
  <li>The victim had totally lost control
before contact and consequently his line was unpredicable. The shunter was
    in full control.</li>
</ol>

<P>Avoidable Rear End Shunt:  Caution
<ol>
  <li> The victim was trying to lap a slower car and the shunter didn't allow the
  victim the opportunity to change his line if necessary. Both the victim
    and the shunter were in full control.</li>
  <li>The victim slowed unexpectedly and the shunter was too close to take
    avoiding action. Typical situations could be:
    <ul>
      <li>The victim was preparing to enter the pit lane but slowed far too
        early.</li>
      <li>The victim slowed down for an incident for which the shunter was
        unsighted.</li>
    </ul>
  </li>
</ol>

<P>Unavoidable Rear End Shunt:  No Penalty - Racing Incident
<ol>
  <li> The victim had performed a shift R and respawned in front of the shunter.</li>
  <li> The victim had totally lost control, left the track and bounced back on
  coming to rest in front of the shunter.</li>
</ol>