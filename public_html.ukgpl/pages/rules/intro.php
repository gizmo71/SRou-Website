<H1 ALIGN="CENTER"><U>Introduction</U></H1>

<P>As with all forms of real motorsport, Sim Racing at UKGPL is conducted in accordance with a set of published rules and regulations.  Many of the rules and regulations in real motorsport are there to ensure the safety of the drivers, officials and general public.  The rules also encourage fair competition and the regulations facilitate different classes of races.  Whilst safety (in the virtual world) is not a major consideration in Sim Racing; authenticity, fair competition and categories of racing certainly is.  Consequently many of the rules applicable in real motorsport are also applicable in Sim Racing but additional rules are also necessary to address the particular issues associated with Sim Racing on-line.</P>
<P>
At UKGPL there are two forms of rules and regulations:
<ul>
<li>Generic</li>
<li>Division Specific</li>
</ul>
<P>
<b>Please Note: </b>The Division Specific rules normally complement the Generic rules but where clashes or contradictions exist, the Division Specific rules will always take precedence over the Generic rules.
</P>

The generic rules and regulations cover all aspects of Sim Racing at UKGPL.  They are described below and can also be accessed via the navigation links at the top of this page. The rules and regulations which are specific to each division are published on the championship page for that division.  Every division has a championship page which holds the results table.  These division specific rules can vary tremendously and often change from season to season.
</P>
<P>
The generic rules and regulations are divided into several categories:
<ul>
<li><A HREF="structure">Structure</A> - Describes how UKGPL is organised and run.</li>
<li><A HREF="scoring">Scoring</A> - Defines the scoring systems used for individual and team championships.</li>
<li><A HREF="game">Game</A> - Defines the context in which the game "Grand Prix Legends" is used at UKGPL including the modifications that are allowed and those which are not.</li>
<li><A HREF="online">On-Line</A> - Defines the procedures, protocols and conventions that must be adopted when playing "Grand Prix Legends" on-line to ensure a reliable connection.</li>
<li><A HREF="etiquette">Etiquette</A> - Defines how drivers should behave on-line to avoid the potential problems introduced by internet lag and the limitations of the simulation, particularly the limited visibility and inescapable blind spots.</li>
<li><A HREF="penalties">Penalty Guidelines</A> - Provides guidance for the moderators to ensure consistent and fair adjudication of incidents.
</UL>
</P>
