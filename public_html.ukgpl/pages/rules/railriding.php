<H3>Railriding</H3>

<p>It is impossible to moderate this type of behaviour from reviewing a single incident. Some of the moderating techniques which rely on analyzing GPL
Race Analyzer reports or graphs derived from them will not identify this problem. The only way to confirm railriding is to carry out a
comprehensive race review. This is documented as <a href="/index.php/moderating/moderatingarace#modmethod1"> Method 1</a> on the pages
describing how to moderate a race.</p>

<h4>Typical Scenarios:</h4>

Persistent Railriding: Exclusion
<ol>
  <li>Continual deliberate railriding at every opportunity.</li>
</ol>

Excessive Railriding: Penalty 3 places
<ol>
  <li>Consistent railriding at specific places on the lap.</li>
</ol>

Simple Railriding: Penalty 2 places
<ol>
  <li>Occasional railriding at specific places on the lap.</li>
  <li>Persistent hitting of barriers.</li>
</ol>

<P>Marginal Railriding: Penalty 1 place
<ol>
  <li>Occasional railriding at random places on the lap.</li>
</ol>