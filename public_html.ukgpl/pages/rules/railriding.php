<H3> Railriding and Corner Cutting</H3>

<p>It is impossible to moderate this type of behaviour from reviewing a single
incident. Some of the moderating techniques which rely on analyzing GPL
Race Analyzer reports or graphs derived from them will not identify this
problem. The only way to confirm railriding is to carry out a
comprehensive race review. This is documented as <a href="/index.php/moderating/moderatingarace#modmethod1"> Method 1</a> on the pages
describing how to moderate a race.</p>

<p>Corner cutting can be identified as placing three or four wheels outside the track boundary.</p>

<h4>Typical Scenarios:</h4>

<p>Persistent Railriding or Corner Cutting: Penalty 3 places
<ol>
<li>Continual deliberate railriding at every opportunity.</li>
<li>Continual deliberate corner cutting INCLUDING attempts at or actual overtaking using this method resulting in a collision.</li>
</ol>

<p>Excessive Railriding or Corner Cutting: Penalty 2 places
<ol>
<li>Consistent railriding or corner cutting at specific places on the lap.</li>
</ol>

<p>Simple Railriding or Corner Cutting: Penalty 1 place
<ol>
<li>Occasional railriding or corner cutting at specific places on the lap.</li>
<li>Persistent hitting of barriers.</li>
</ol>

<p>Marginal Railriding: Warning
<ol>
<li>Occasional railriding at random places on the lap.</li>
</ol>

<p>Accidental Corner Cutting: Caution
<ol>
<li>Occasional and unintentional corner cutting at random places on the lap.</li>
</ol>

<p>The moderators also reserve the right to impose additional time penalties where it is felt an advantage has been gained.
