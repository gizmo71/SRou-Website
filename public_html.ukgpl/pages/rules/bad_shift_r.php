<H3>Bad Shift R</H3>

<P>This happens with regular monotony. It is one of the rare occasions
when a driver is in full control of the situation, he cannot blame a bad shift R
on the actions of others. Drivers quite often rush into performing a shift
R when they should wait and assess the situation first.

<P>The key features to consider here are:
<ul>
  <li>Did the driver manoeuvre his damaged car to a place where he knew his car
    would respawn off the racing line?</li>
  <li>Did the driver wait until he was sure there were no approaching cars
    before he took a shift R?</li>
</ul>

<h4>Typical Scenarios:</h4>

<P>Blatant Bad Shift R: Penalty 3 Places
<ol>
  <li> The resetting driver did not consider where his car would respawn and was
  not cognizant of approaching cars. Contact was made because other
  drivers had no chance of avoiding the respawned car.</li>
</ol>

<P>Simple Bad Shift R: Penalty 2 Places
<ol>
  <li> The resetting driver DID consider where his car would respawn but was
  unaware of any other cars (couldn't see or
     hear, or expect another car to be near). Contact was made because
  other drivers had no chance of avoiding the respawned car.</li>
  <li> The resetting driver did not consider where his car would respawn and was
  not cognizant of approaching cars. Contact was NOT made but other
  drivers, who had slowed down for the yellow flags, had to take drastic evasive
  action.</li>
</ol>

<P>Inconsiderate Bad Shift R: Penalty 1 Place
<ol>
  <li> The resetting driver did not consider where his car would respawn and was
  not cognizant of approaching cars. Contact was NOT made but other
  drivers, who had NOT slowed down for the yellow flags, had to take evasive
  action.</li>
</ol>

<P>Incompetent Bad Shift R: Warning
<ol>
  <li> The resetting driver DID consider where his car would respawn but was
  unaware of any other cars (couldn't see or
     hear, or expect another car to be near). Contact was NOT made but
  other drivers, who HAD slowed down for the yellow flags, had to take drastic
  evasive action.</li>
</ol>

<P>Undesirable Bad Shift R: Caution
<ol>
  <li> The resetting driver DID consider where his car would respawn but was
  unaware of any other cars (couldn't see or
     hear, or expect another car to be near). Contact was NOT made but
  other drivers, who had NOT slowed down for the yellow flags, had to take
  evasive action.</li>
</ol>

<P>Unfortunate Bad Shift R: Racing Incident
<ol>
  <li> The resetting driver had no choice. The most likely scenario for this
  incident is a mass crash at turn 1 on lap 1 where respawned cars are likely to
  be hit.</li>
</ol>