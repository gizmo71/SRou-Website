<H3>Contravention of Sporting Regulations</H3>

<h4>Changing Cars after Joining Server</h4>

<P>The primary goal of this rule is to prevent drivers continually swapping
chassis depending on what the other competitors are choosing. The drivers
can do this to some extent anyway by waiting in VROC until everyone else has
joined and then making a decision on what chassis to take. The effect that
setting a time and then changing chassis has is less predictable. The
server may reserve a place on the grid for someone who set a time in a chassis
which they subsequently abandoned in preference to another. Given that the
effect on the server and consequently on other competitors is unpredictable, the
award of penalties must be at the discretion of the moderator.

<h4>Typical Scenarios:</h4>
<P>Frivolous Car Changing: Exclusion
<ol>
  <li>Setting several times in various chassis.</li>
  <li>Continually swapping between different chassis.</li>
</ol>
<p>Excessive Car Changing: Penalty 3 Places</p>
<ol>
  <li>Setting times in 3 different chassis.</li>
</ol>
<p>Simple Car Changing: Penalty 2 Places.</p>
<ol>
  <li>Setting times in 2 different chassis.</li>
  <li>Leaving the pits in 3 or more chassis but only setting a time in one.</li>
</ol>
<p>Avoidable Car Changing: Caution.</p>
<ol>
  <li>Setting times in 2 chassis but one was so slow to be well off the back of
    the grid.</li>
  <li>Leaving the pits in 2 chassis but only setting a time in one.</li>
</ol>