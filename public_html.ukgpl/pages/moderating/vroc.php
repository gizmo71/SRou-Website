<H1 ALIGN="CENTER">VROC</H1>

<p>These instructions show how to set up a race at Monza on 1.UKGPL. The passwords have been omitted for obvious reasons. Other servers used by UKGPL can be started in the same way but the IP Address and possibly the port number will be different.</p>

<p>1. Open up VROC and select &quot;Launch remote GPL...&quot; from the Host drop down menu:</p>
