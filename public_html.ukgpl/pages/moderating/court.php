    </blockquote>
  </blockquote>
</blockquote>

<p>3. After the drivers have been given enough time to submit incident reports (normally about a week) the divisional moderator will go into the court system and begin the moderation 
	process by selecting the race from the court.</p>
<blockquote>
  <blockquote>
    <blockquote>
