<H1 ALIGN="CENTER">Court System</H1>

<p>UKGPL uses the SRou court system to manage incident reports.  The court system allows drivers to submit incident reports in private for consideration by the moderating team. 
	The Divisional Moderator is responsible for collating the incident reports, analysing the incidents and compiling a report but invariably he will ask for second opinions from
	 the other moderators thereby ensuring a balanced and well reasoned report.</p>
	 
<p>Moderating races is not easy but the task can be made less arduous if drivers submit accurate and informative incident reports. Understanding how the court system works should help 
	drivers compose better incident reports.</p>

<p>1. After the race results are imported into SRou, a results table appears on the race thread.  Under the results table is a hyper-link which allows the drivers  
	to submit incident reports:</p>
<blockquote>
  <blockquote>
    <blockquote>
      <p><img border="0" src="/images/court/submit_report.jpg" width="424" height="144"></p>
      <p></p>
    </blockquote>
  </blockquote>
</blockquote>

<p>2. The incident report form is self explanatory.  The important point to note is that the details are only visible to the moderating team not to the other drivers. 
	This ensures drivers can be open and honest without the possibility of causing offence or disagreements on the drivers forum. It is for the moderator to decide 
	what to do about the incident once the views of all the drivers who have submitted the reports has been taken into account.  It is helpful if drivers enter the time 
	at which the incident occurred in the format &#42&#42h&#42&#42m&#42&#42s because that is the format used in GPL Replays and it ensures the various reports can be 
	associated with the correct incident.</p>
<blockquote>
  <blockquote>
    <blockquote>
