<p>4. Once the moderation process is underway the drivers cannot submit any more incident reports and a notice to that effect appears under the results table.</p>
<blockquote>
  <blockquote>
    <blockquote>
    <p><img border="0" src="/images/court/provisional_results_notice.jpg" width="424" height="82"></p>
    </blockquote>
  </blockquote>
