<H1 ALIGN="CENTER">Sporting Regulations</H1>

<H2>General</H2>

<UL>

<LI><P>When joining a race, please use a driver name which is easily recognisable so that we can keep the score sheet accurate, eg. Marc Mercer could use Marc2 Mercer, Marc Mercer2 or something similar.</P>

<BLOCKQUOTE>If GPL is not recording the driver's lap times in qualifying, they may disconnect then reconnect using a slight change in name; this may solve the problem.</BLOCKQUOTE>

<LI><P>Once the driver has entered the server their car choice is made. They may not exit and change car after they have joined the server. The only exception to this is if WinVROC or the driver accidentally selects the wrong car. In this case they must exit <I>immediately</I> (without driving any laps) and change to the correct car.</P>

<BLOCKQUOTE>Any driver who sets a time and then changes car will be penalized. Please see the <a HREF="contravene_sporting">Penalty Guidelines</a> for more details.</BLOCKQUOTE>

</UL>

<H2>In-game chat</H2>

<UL>

<LI><P>As a general rule, chat is <B>not</B> allowed during league races, except after all drivers on track have passed the chequered flag. This applies to both qualification and race sessions.</P>

<LI><P>The only exceptions to this are "PO" or similar for exiting the pits, "PI" for entering the pits and "S" to apologize after an accident.  Even using "S" after an accident is not encouraged; a driver should be able to apologize after the race, any unnecessary chatting is distracting to the other drivers.</P>

<BLOCKQUOTE>Drivers should not forget that when they have finished their race it will usually be another minute or two before all the other drivers have finished theirs, so they should not start chatting before everyone has finished.</BLOCKQUOTE>

</UL>

<H2>Timing and scoring mistakes caused by network problems</H2>

<BLOCKQUOTE>This section is intended to ensure that no driver is unnecessarily penalized by internet problems such as warp and clock smashes.</BLOCKQUOTE>

<UL>

<LI><P>If a driver gains pole position (or another artificially high grid slot) because of a warp, they are not required to forfeit this position.</P>

<BLOCKQUOTE>A driver who finds themselves in an unexpectedly high grid slot is expected to exercise great caution at the start, where faster drivers may be "bottled up" behind them.</BLOCKQUOTE>

<LI><P>If a driver receives an <I>undeserved black flag</I> (that is, a black flag because of a network problem) or is lost by the server, they may choose to ignore this and continue racing. After the race, they should inform the moderator, who will check the replay and reinstate them in the results as appropriate.</P>

<BLOCKQUOTE>Drivers who have been disqualified or forgotten by the server should remember that other drivers will not see them on their lap boards, nor be shown blue flags if they are being lapped, and should adjust their expectations of other drivers' behaviour towards them accordingly.</BLOCKQUOTE>

</UL>

<H2>Incidents involving other cars</H2>

<UL>

<LI><P>If a driver is involved in an incident with another car, they must report the incident to the division moderator using the incident reporting system in SRou.  The reporting system can be accessed via a link entitled "Submit an incident report" that will be made available under the race results table.  Failure to do so will leave the driver with no appeal against any penalty assessed. The driver should try to save a replay of the incident if possible, as this will help their case.  These replays can be uploaded onto the incident reporting system but they should be kept as short as possible and must always be zipped.  The other moderators will then hold a "stewards enquiry" and decide on any penalties that may be applicable.</P>

<BLOCKQUOTE>If a driver hits another driver and cause an accident, it is suggested that they do not attempt to wait for them, as this is likely to cause more problems for drivers who are passing the scene of the accident as two or more cars all try to rejoin "in order".</BLOCKQUOTE>

</UL>

<H2>Shift-R (resetting the car)</H2>

<UL>

<H3>General Rules</H3>

<UL>

<LI><P>For divisions that don't run under PRO rules (where Shift-Rs are not possible), a Shift-R is allowed <I>only</I> in a race, not in practice.</P>

<BLOCKQUOTE>This rule may be relaxed at certain tracks by the moderator (usually only the Nurburgring).</BLOCKQUOTE>

<LI><P>Following a Shift-R a driver should make a Stop and Go (&quot;S&amp;G&quot;) in the pits.  However if a driver was forced to do a Shift-R through no fault of their own (for example being blatantly knocked off the track by another driver - please see the <A HREF="penalties#nostopgo">Penalty Guidelines</A> for more details) they are exempt from having to make a S&amp;G.  This is commonly referred to as making a "no-fault Shift-R".  After the race, the moderator will decide if the "no-fault Shift-R" was justified and if not will award an appropriate time penalty - see below.</P>

<BLOCKQUOTE>A S&amp;G <I>must</I> be taken within 2 laps of the Shift-R, and <I>cannot</I> be taken as part of a normal <A HREF="pitstops"> timed pit stop</A>.</BLOCKQUOTE>

<LI><P>Drivers should take every precaution to make sure they rejoin the race without interfering with any other drivers. This means waiting until there is a sufficient gap in the traffic before using Shift-R. On lap 1 this may even mean waiting until the whole field has passed before using Shift-R.</P>

<BLOCKQUOTE>Any driver who, due to inappropriate use of Shift-R, causes another car to crash or take severe avoiding action will be penalised.</BLOCKQUOTE>

</UL>

<H3>Fuel and Tyre Pit Stops</H3>

<UL>

<li>

<P>For the divisions that allow them, a fuel and tyre pit stop must be carried out in the pit lane.  The <A HREF="pitstops">normal rules</A> for how to perform a pit stops, i.e. approach, position in pit lane etc still apply but not the pit window.  This means that you will be able to stop for fuel/tyres at any time during the race.  Obviously a Shift-R for a Fuel and Tyres carries no penalty.</P>

<BLOCKQUOTE>All drivers need to be extra careful as they pass the pitlane to ensure they avoid the respawn area (check in advance if you are not sure where this is but as a general rule it's off the racing line).</BLOCKQUOTE>

</li>

</UL>

<H3>Time Penalties</H3>

<UL>

<LI><P>When awarded, the time penalties will vary depending on whether or not the Shift-R was made as a result of a contested pass, driver error or mechanical failure. If a driver is culpable in any way and has not chosen to make a S&amp;G then for situations where the Shift-R was taken as a result of an incident involving another driver, a 15 second time penalty will be awarded.  For situations where clearly no other car was involved (e.g. mechanical problems or becoming beached on a banking) a 30 second time penalty will be awarded.</P>

<LI><P>For incidents involving another driver, you only have to S&amp;G if you were in some way responsible for incident. If you are not sure, DON'T S&amp;G.</P>

<BLOCKQUOTE>A S&amp;G usually costs you about 10 secs so you only stand to lose a further 5 secs if you don't S&amp;G and it turns out to be your fault (in addition to any place penalties applied).</BLOCKQUOTE>

<li><P>For incidents NOT involving another driver then it can't be anyone's fault but your own. In these cases you would be well advised to take a S&amp;G.</P>

<BLOCKQUOTE>A S&amp;G usually costs you about 10 secs so you stand to lose a further 20 secs if you don't S&amp;G.</BLOCKQUOTE>

</li>

</UL>
</UL>

<H2>Rejoining the race after an incident</H2>

<UL>

<LI><P>It is the responsibility of any driver who is returning to the track to make sure they do not interfere with other drivers.</P>

<BLOCKQUOTE>Rejoining drivers should stay off the racing line and give way to faster cars until they are fully up to speed. Rejoining drivers should not attempt to keep anyone behind them until they are fully up to speed. Any driver who rejoins a race and, owing to negligence, causes another car to crash or take severe avoiding action, will be penalised.</BLOCKQUOTE>

</UL>

<H2>Lapping</H2>

<UL>

<LI><P>Any driver who is about to be lapped may not deliberately block or race the lapping driver, and must allow the lapping driver to pass at the first safe opportunity.</P>

<LI><P>Any driver intending to lap another car must allow for warping, the difference in speed (especially under braking), and must execute any pass safely and without contact.</P>

</UL>

<H2>Flags and pit signals</H2>

<UL>

<H3>Deserved black flags</H3>

<UL>

<LI><P>A driver being shown the black flag and a "pit in" board for a genuine offence, such as jumping the start, must enter the pits and take the penalty. Failure to do so will result in the disqualification of that driver.</P>

</UL>

<H3>Yellow and green flags</H3>

<UL>

<LI><P>Drivers have a duty to drive safely and well within their limits when they have passed a yellow flag. Stay on the racing line unless it is blocked. Drivers may <B>not</B> overtake any car they are racing with, until they are sure they have passed a green flag <B>or</B> they are sure they have passed the incident and the green flag has been taken in. (Sometimes a yellow flag is shown when a car loses and then quickly regains control, so the green flag may be gone by the time the driver passes the next marshall).</P>

</UL>
</UL>