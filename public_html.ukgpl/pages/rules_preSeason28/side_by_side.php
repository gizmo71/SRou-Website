<H3>Side by Side Contact</H3>

<p>The limited lateral view in GPL makes incidents of this type very
likely. However they can be avoided if drivers are cognizant of the
position, or expected position, of other cars. This requires the
development of a sixth sense to be sure where all the other cars are, especially
on the first lap. However, if in doubt drivers should always leave room.

<p>The key features to consider
here are:
<ul>
  <li>Was the shunter in control?</li>
  <li>Was the victim in control?</li>
  <li>How much overlap had been achieved?</li>
</ul>

<h4>Typical Scenarios:</h4>

<P>Blatant Side by Side contact:  Penalty 2 Places
<ol>
  <li> On a straight, the overtaking car moves out to overtake but cuts back in
  too early. The victim made no mistakes and was driving their normal line at normal speed.
    The shunter made no allowance for the victim whatsoever and was in full 
control when the incident took place. There was no loss of control. Penalty for overtaking
  driver.</li>
  <li> On a straight, the overtaking car moves out to overtake, maintains his line
  and has achieved 100% overlap but the overtaken driver moves across into
  him. There was no loss of control. Penalty for overtaken driver.</li>
</ol>

<P>Simple Side by Side contact:  Penalty 1 Place
<ol>
  <li> On a straight, the overtaking car moves out to overtake but cuts back in
  too early. There was some loss of control. Penalty for overtaking
  driver.</li>
  <li> On a straight, the overtaking car moves out to overtake, maintains his line
  and has NOT achieved 100% overlap but the overtaken driver moves across into
  him. Penalty for overtaken driver.</li>
  <li> On a straight, the overtaking car moves out to overtake and both drivers
  drift off line. Penalty for both drivers.</li>
  <li> In a corner, after a legitimate overtaking attempt the cars end up side
    by side. One driver moves over instead of staying on their own side of
    the track. Contact is made. Penalty for driver that moves over.</li>
</ol>

<P>Marginal Side by Side contact:  Warning
<ol>
  <li>In a corner, after a legitimate overtaking attempt the cars end up side by
    side. One driver looses control and strays from their side of the
    track. Contact is made. Warning for driver that moves over.</li>
</ol>

<P>Contested Side by Side contact: Racing Incident
<ol>
  <li>In a corner, after a legitimate overtaking attempt the cars end up side by
    side. Both drivers move over instead of staying on their own side of
    the track. Contact is made. Racing Incident.</li>
</ol>